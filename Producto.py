

class Producto:

	def __init__(self):
		pass

	def setnombre(self, _nombre):
		self.nombre = _nombre

	def setcategoria(self, _categoria):
		self.categoria = _categoria

	def setprecio(self, _precio):
		self.precio = _precio

	def setstock(self, _stock):
		self.stock = _stock


	def getnombre(self):
		return self.nombre

	def getcategoria(self):
		return self.categoria

	def getprecio(self):
		return self.precio

	def getstock(self):
		return self.stock

